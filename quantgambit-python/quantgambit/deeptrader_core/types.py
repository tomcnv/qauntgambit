import logging

# Module-level logger for deprecation warnings
_logger = logging.getLogger(__name__)

# Track which deprecation warnings have been logged (once per session)
_deprecation_warnings_logged: Set[str] = set()


def _log_deprecation_warning(old_name: str, new_name: str) -> None:
    """
    Log a deprecation warning once per session for a renamed field.
    
    This helper ensures that deprecation warnings are only logged once per
    field name per session to avoid log spam.
    
    Args:
        old_name: The deprecated field name
        new_name: The new field name to use instead
    """
    if old_name not in _deprecation_warnings_logged:
        _deprecation_warnings_logged.add(old_name)
        msg = f"MarketSnapshot.{old_name} is deprecated, use {new_name} instead"
        _logger.warning(msg)
        warnings.warn(msg, DeprecationWarning, stacklevel=3)


