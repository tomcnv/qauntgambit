@dataclass(frozen=True)
class MarketSnapshot:
    """
    Frozen, immutable snapshot of market state for decision making.
    
    Every stage in the pipeline reads from this snapshot to ensure consistency.
    Created once per tick by SnapshotBuilderStage.
    
    Strategy Signal Architecture Fixes - Requirement 3.11:
    - Added flow_rotation (EWMA smoothed orderflow signal)
    - Added trend_bias (HTF trend signal)
    - rotation_factor retained for backward compatibility (deprecated)
    
    Distance Field Semantics:
    - distance_to_poc_bps: SIGNED distance. Positive = price above POC, negative = price below POC.
      Formula: (price - poc) / mid_price * 10000
    - distance_to_vah_bps: ABSOLUTE distance. Always >= 0.
      Formula: abs(price - vah) / mid_price * 10000
    - distance_to_val_bps: ABSOLUTE distance. Always >= 0.
      Formula: abs(price - val) / mid_price * 10000
    
    Slippage Semantics:
    - expected_fill_slippage_bps: ONE-WAY slippage (entry fill only).
      Includes half-spread + depth/freshness/volatility penalties.
      For round-trip costs, multiply by 2 or add entry + exit separately.
    """
    # Identity
    symbol: str
    exchange: str
    timestamp_ns: int
    snapshot_age_ms: float
    
    # Price
    mid_price: float
    bid: float
    ask: float
    spread_bps: float
    
    # Depth
    bid_depth_usd: float
    ask_depth_usd: float
    depth_imbalance: float  # -1 to +1
    
    # Orderflow (multi-timeframe)
    imb_1s: float
    imb_5s: float
    imb_30s: float
    orderflow_persistence_sec: float
    
    # Volatility
    rv_1s: float
    rv_10s: float
    rv_1m: float
    vol_shock: bool  # rv_1s > rv_10s * 3
    
    # Regime (numeric scores + string labels)
    vol_regime: str  # "low", "normal", "high", "extreme"
    vol_regime_score: float  # 0-1
    trend_direction: str  # "up", "down", "neutral"
    trend_strength: float  # 0-1
    
    # Volume profile
    poc_price: Optional[float]
    vah_price: Optional[float]
    val_price: Optional[float]
    position_in_value: str  # "above", "below", "inside"
    
    # Execution estimates
    expected_fill_slippage_bps: float
    typical_spread_bps: float  # Rolling average for comparison
    
    # Data quality
    data_quality_score: float  # 0-1
    ws_connected: bool
    
    # AMT distance and rotation fields (Requirements 5.1, 5.2)
    # These have defaults since they may not be available when AMT levels are missing
    # Field names include _bps suffix to indicate units (basis points)
    # SIGNED: positive = price above level, negative = price below level
    distance_to_poc_bps: float = 0.0  # SIGNED: (price - poc) / mid_price * 10000
    # ABSOLUTE: always >= 0, measures distance regardless of direction
    distance_to_vah_bps: float = 0.0  # ABSOLUTE: abs(price - vah) / mid_price * 10000
    distance_to_val_bps: float = 0.0  # ABSOLUTE: abs(price - val) / mid_price * 10000
    
    # Split rotation signals (Strategy Signal Architecture Fixes - Requirement 3.11)
    flow_rotation: float = 0.0     # EWMA smoothed orderflow signal [-5, +5]
    trend_bias: float = 0.0        # HTF trend signal [-1, +1]
    rotation_factor: float = 0.0   # DEPRECATED: Legacy combined signal (-15 to +15)
    
    # Backward compatibility properties for deprecated field names (Requirement 5.1, 5.2)
    @property
    def distance_to_poc(self) -> float:
        """DEPRECATED: Use distance_to_poc_bps instead."""
        _log_deprecation_warning("distance_to_poc", "distance_to_poc_bps")
        return self.distance_to_poc_bps
    
    @property
    def distance_to_vah(self) -> float:
        """DEPRECATED: Use distance_to_vah_bps instead."""
        _log_deprecation_warning("distance_to_vah", "distance_to_vah_bps")
        return self.distance_to_vah_bps
    
    @property
    def distance_to_val(self) -> float:
        """DEPRECATED: Use distance_to_val_bps instead."""
        _log_deprecation_warning("distance_to_val", "distance_to_val_bps")
        return self.distance_to_val_bps
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dict for telemetry (key stats only)."""
        return {
            "symbol": self.symbol,
            "timestamp_ns": self.timestamp_ns,
            "snapshot_age_ms": self.snapshot_age_ms,
            "mid_price": self.mid_price,
            "spread_bps": self.spread_bps,
            "imb_5s": self.imb_5s,
            "vol_shock": self.vol_shock,
            "vol_regime": self.vol_regime,
            "trend_direction": self.trend_direction,
            "trend_strength": self.trend_strength,
            "data_quality_score": self.data_quality_score,
            # AMT fields (Requirements 6.1, 6.2, 6.3, 6.4)
            "poc_price": self.poc_price,
            "vah_price": self.vah_price,
            "val_price": self.val_price,
            "position_in_value": self.position_in_value,
            "distance_to_poc_bps": self.distance_to_poc_bps,
            "distance_to_vah_bps": self.distance_to_vah_bps,
            "distance_to_val_bps": self.distance_to_val_bps,
            # Split rotation signals (Requirement 3.11)
            "flow_rotation": self.flow_rotation,
            "trend_bias": self.trend_bias,
            "rotation_factor": self.rotation_factor,
        }


