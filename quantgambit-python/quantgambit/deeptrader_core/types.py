@dataclass
class CandidateSignal:
    """
    Intermediate signal representing a geometric setup before confirmation.
    
    This dataclass represents a trading opportunity detected by a strategy
    that has not yet been confirmed by flow/trend validation. It supports
    EITHER distance-based OR price-based SL/TP specification.
    
    Use normalize() to convert between formats.
    
    Requirement 4.1: CandidateSignal dataclass with required fields
    Requirement 4.2: Support EITHER distance-based OR price-based specification
    Requirement 4.3: Provide normalize() for conversion
    
    Attributes:
        symbol: Trading symbol (e.g., "BTCUSDT")
        side: Trade direction - "long" or "short"
        strategy_id: Identifier of the strategy that generated this candidate
        profile_id: Identifier of the trading profile
        entry_price: Proposed entry price
        sl_distance_bps: Stop loss distance in basis points (optional)
        tp_distance_bps: Take profit distance in basis points (optional)
        sl_price: Stop loss price (optional)
        tp_price: Take profit price (optional)
        setup_reason: Human-readable reason for the setup
        setup_score: Quality score for the setup (0-1, higher is better)
        requires_flow_reversal: Whether flow reversal is required for confirmation
        flow_direction_required: Required flow direction ("positive", "negative", or None)
        max_adverse_trend_bias: Maximum adverse trend bias allowed
        setup_timestamp_ns: Timestamp when setup was detected (nanoseconds)
    """
    symbol: str
    side: str  # "long" or "short"
    strategy_id: str
    profile_id: str
    entry_price: float
    
    # SL/TP - support EITHER distances OR prices (Requirement 4.2)
    sl_distance_bps: Optional[float] = None
    tp_distance_bps: Optional[float] = None
    sl_price: Optional[float] = None
    tp_price: Optional[float] = None
    
    # Setup metadata (Requirement 4.1)
    setup_reason: str = ""
    setup_score: float = 0.5  # 0-1 confidence in setup quality
    
    # Confirmation requirements (Requirement 4.11)
    requires_flow_reversal: bool = True
    flow_direction_required: Optional[str] = None  # "positive", "negative"
    max_adverse_trend_bias: float = 0.5
    
    # Dual threshold for EVGate (Requirement 2.11)
    # profitability_threshold_bps is passed to EVGate for economic viability check
    profitability_threshold_bps: Optional[float] = None
    
    # Timestamps
    setup_timestamp_ns: int = field(default_factory=lambda: int(time.time() * 1e9))
    
    def normalize(self, mid_price: float) -> "CandidateSignal":
        """
        Normalize to have both distance and price representations.
        
        If distances provided, calculates prices.
        If prices provided, calculates distances.
        Uses canonical formula: distance_bps = |price - entry| / mid_price * 10000
        
        Requirement 4.3: CandidateSignal SHALL provide normalize() that converts
        prices to distances or vice versa.
        
        Args:
            mid_price: Mid price for normalization (best_bid + best_ask) / 2
            
        Returns:
            New CandidateSignal with both distance and price fields populated
        """
        # Create a copy with updated values
        new_sl_distance_bps = self.sl_distance_bps
        new_tp_distance_bps = self.tp_distance_bps
        new_sl_price = self.sl_price
        new_tp_price = self.tp_price
        
        # Handle SL conversion
        if self.sl_distance_bps is not None and self.sl_price is None:
            # Convert distance to price
            if self.side == "long":
                new_sl_price = self.entry_price * (1 - self.sl_distance_bps / 10000)
            else:
                new_sl_price = self.entry_price * (1 + self.sl_distance_bps / 10000)
        elif self.sl_price is not None and self.sl_distance_bps is None:
            # Convert price to distance using canonical formula
            if mid_price > 0:
                new_sl_distance_bps = abs(self.entry_price - self.sl_price) / mid_price * 10000
            else:
                new_sl_distance_bps = 0.0
        
        # Handle TP conversion
        if self.tp_distance_bps is not None and self.tp_price is None:
            # Convert distance to price
            if self.side == "long":
                new_tp_price = self.entry_price * (1 + self.tp_distance_bps / 10000)
            else:
                new_tp_price = self.entry_price * (1 - self.tp_distance_bps / 10000)
        elif self.tp_price is not None and self.tp_distance_bps is None:
            # Convert price to distance using canonical formula
            if mid_price > 0:
                new_tp_distance_bps = abs(self.tp_price - self.entry_price) / mid_price * 10000
            else:
                new_tp_distance_bps = 0.0
        
        return CandidateSignal(
            symbol=self.symbol,
            side=self.side,
            strategy_id=self.strategy_id,
            profile_id=self.profile_id,
            entry_price=self.entry_price,
            sl_distance_bps=new_sl_distance_bps,
            tp_distance_bps=new_tp_distance_bps,
            sl_price=new_sl_price,
            tp_price=new_tp_price,
            setup_reason=self.setup_reason,
            setup_score=self.setup_score,
            requires_flow_reversal=self.requires_flow_reversal,
            flow_direction_required=self.flow_direction_required,
            max_adverse_trend_bias=self.max_adverse_trend_bias,
            profitability_threshold_bps=self.profitability_threshold_bps,
            setup_timestamp_ns=self.setup_timestamp_ns,
        )
    
    def to_strategy_signal(
        self,
        size: float,
        confirmation_reason: str,
    ) -> StrategySignal:
        """
        Convert to StrategySignal after confirmation.
        
        Requirement 4.7: Confirmation_Stage SHALL convert confirmed candidates
        to StrategySignal with SL/TP prices.
        
        Args:
            size: Position size for the trade
            confirmation_reason: Reason for confirmation (e.g., "flow=1.5,trend=0.2")
            
        Returns:
            StrategySignal ready for EVGate and execution
            
        Raises:
            ValueError: If sl_price or tp_price is not set (call normalize() first)
        """
        if self.sl_price is None:
            raise ValueError("sl_price must be set before converting to StrategySignal. Call normalize() first.")
        if self.tp_price is None:
            raise ValueError("tp_price must be set before converting to StrategySignal. Call normalize() first.")
        
        return StrategySignal(
            strategy_id=self.strategy_id,
            symbol=self.symbol,
            side=self.side,
            size=size,
            entry_price=self.entry_price,
            stop_loss=self.sl_price,
            take_profit=self.tp_price,
            meta_reason=f"{self.setup_reason}|{confirmation_reason}",
            profile_id=self.profile_id,
        )
    
    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary for telemetry."""
        return {
            "symbol": self.symbol,
            "side": self.side,
            "strategy_id": self.strategy_id,
            "profile_id": self.profile_id,
            "entry_price": self.entry_price,
            "sl_distance_bps": self.sl_distance_bps,
            "tp_distance_bps": self.tp_distance_bps,
            "sl_price": self.sl_price,
            "tp_price": self.tp_price,
            "setup_reason": self.setup_reason,
            "setup_score": self.setup_score,
            "requires_flow_reversal": self.requires_flow_reversal,
            "flow_direction_required": self.flow_direction_required,
            "max_adverse_trend_bias": self.max_adverse_trend_bias,
            "setup_timestamp_ns": self.setup_timestamp_ns,
        }


class CandidateArbitrator:
    """
    Selects best candidate when multiple strategies emit for same symbol.
    
    Requirement 4.5: When multiple strategies emit candidates for the same symbol,
    the Candidate_Arbitrator SHALL select the best candidate based on:
    - setup_score (higher is better)
    - expected_ev (if available)
    - strategy_priority (configurable per strategy)
    
    Attributes:
        strategy_priorities: Dict mapping strategy_id to priority (higher is better)
    """
    
    def __init__(self, strategy_priorities: Optional[Dict[str, int]] = None):
        """
        Initialize the arbitrator.
        
        Args:
            strategy_priorities: Dict mapping strategy_id to priority.
                Higher priority values are preferred. Default priority is 0.
        """
        self.strategy_priorities = strategy_priorities or {}
    
    def select_best(
        self,
        candidates: List[CandidateSignal],
    ) -> Optional[CandidateSignal]:
        """
        Select best candidate based on setup_score and strategy priority.
        
        Requirement 4.5: Selection criteria:
        1. setup_score (higher is better) - primary criterion
        2. strategy_priority (higher is better) - tiebreaker
        
        Args:
            candidates: List of CandidateSignal objects to choose from
            
        Returns:
            Best CandidateSignal, or None if candidates list is empty
        """
        if not candidates:
            return None
        
        def score_candidate(c: CandidateSignal) -> tuple:
            """Score a candidate for comparison. Higher is better."""
            priority = self.strategy_priorities.get(c.strategy_id, 0)
            # Return tuple for comparison: (setup_score, priority)
            # Python compares tuples element by element
            return (c.setup_score, priority)
        
        return max(candidates, key=score_candidate)
    
    def set_priority(self, strategy_id: str, priority: int) -> None:
        """
        Set priority for a strategy.
        
        Args:
            strategy_id: Strategy identifier
            priority: Priority value (higher is better)
        """
        self.strategy_priorities[strategy_id] = priority
    
    def get_priority(self, strategy_id: str) -> int:
        """
        Get priority for a strategy.
        
        Args:
            strategy_id: Strategy identifier
            
        Returns:
            Priority value, or 0 if not set
        """
        return self.strategy_priorities.get(strategy_id, 0)