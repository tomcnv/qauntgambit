class ExitType(Enum):
    """
    Classification of exit reasons.
    
    SAFETY exits bypass min_hold time - they protect from catastrophic losses.
    INVALIDATION exits respect min_hold - they indicate strategy premise changed.
    """
    SAFETY = "safety"              # Hard stop, liquidation, data failure - ignores min_hold
    INVALIDATION = "invalidation"  # Orderflow flip, regime change - respects min_hold


@dataclass
class ExitDecision:
    """
    Structured exit decision with classification.
    
    Safety exits (hard stop, liquidation proximity, data failure) always allowed.
    Invalidation exits (orderflow flip, regime change) respect min_hold.
    """
    should_exit: bool
    exit_type: ExitType
    reason: str
    urgency: float  # 0-1, higher = more urgent
    confirmations: List[str] = field(default_factory=list)
    fee_check_result: Optional[Any] = None  # FeeAwareExitCheck when fee model is used
    fee_check_bypassed: bool = False
    fee_bypass_reason: Optional[str] = None
    
    def bypasses_min_hold(self) -> bool:
        """Safety exits ignore minimum hold time."""
        return self.exit_type == ExitType.SAFETY


