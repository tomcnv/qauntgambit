@dataclass
class SymbolCharacteristics:
    """
    Rolling statistics for a symbol used for parameter adaptation.
    
    Tracks typical spread, depth, volatility, and daily range for each symbol.
    Used by AdaptiveParameterResolver to convert multiplier-based parameters
    to absolute values that are appropriate for each symbol's characteristics.
    
    Requirements: 1.1, 1.2, 1.3, 1.4
    """
    symbol: str
    
    # Spread characteristics (Requirement 1.1)
    typical_spread_bps: float  # EMA of recent spreads
    
    # Depth characteristics (Requirement 1.2)
    typical_depth_usd: float  # EMA of min(bid_depth, ask_depth)
    
    # Volatility characteristics (Requirements 1.3, 1.4)
    typical_daily_range_pct: float  # ATR / price, normalized to daily
    typical_atr: float  # Raw ATR value
    typical_volatility_regime: str  # Mode of recent regimes: "low", "normal", "high"
    
    # Metadata
    sample_count: int  # Number of samples collected
    last_updated_ns: int  # Timestamp of last update
    
    def is_warmed_up(self, min_samples: int = 100) -> bool:
        """
        Check if enough samples collected for reliable estimates.
        
        Args:
            min_samples: Minimum number of samples required (default 100)
            
        Returns:
            True if sample_count >= min_samples
        """
        return self.sample_count >= min_samples
    
    def to_dict(self) -> Dict[str, Any]:
        """
        Convert to dictionary for serialization (e.g., Redis persistence).
        
        Returns:
            Dictionary with all fields as JSON-serializable values
        """
        return {
            "symbol": self.symbol,
            "typical_spread_bps": self.typical_spread_bps,
            "typical_depth_usd": self.typical_depth_usd,
            "typical_daily_range_pct": self.typical_daily_range_pct,
            "typical_atr": self.typical_atr,
            "typical_volatility_regime": self.typical_volatility_regime,
            "sample_count": self.sample_count,
            "last_updated_ns": self.last_updated_ns,
        }
    
    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "SymbolCharacteristics":
        """
        Create SymbolCharacteristics from dictionary.
        
        Args:
            data: Dictionary with all required fields
            
        Returns:
            SymbolCharacteristics instance
        """
        return cls(
            symbol=data["symbol"],
            typical_spread_bps=float(data["typical_spread_bps"]),
            typical_depth_usd=float(data["typical_depth_usd"]),
            typical_daily_range_pct=float(data["typical_daily_range_pct"]),
            typical_atr=float(data["typical_atr"]),
            typical_volatility_regime=str(data["typical_volatility_regime"]),
            sample_count=int(data["sample_count"]),
            last_updated_ns=int(data["last_updated_ns"]),
        )
    
    @classmethod
    def default(cls, symbol: str) -> "SymbolCharacteristics":
        """
        Create default characteristics for cold start.
        
        Used when no historical data is available for a symbol.
        Conservative defaults that work for most crypto assets.
        
        Args:
            symbol: The symbol identifier
            
        Returns:
            SymbolCharacteristics with conservative default values
        """
        return cls(
            symbol=symbol,
            typical_spread_bps=5.0,  # Conservative 5 bps
            typical_depth_usd=50000.0,  # $50k depth
            typical_daily_range_pct=0.03,  # 3% daily range
            typical_atr=0.0,  # Will be calculated
            typical_volatility_regime="normal",
            sample_count=0,
            last_updated_ns=0,
        )


