"""
Core Types - Shared dataclasses for the profile system

Defines the key data structures for profile-based strategy routing:
- Features: Market state snapshot for decision making
- AccountState: Risk and position state
- Profile: Market regime classification
- StrategySignal: Strategy output for execution
- MarketSnapshot: Frozen snapshot for pre-trade gating
- TradeCandidate: Proposed trade with expected edge
- GateDecision: Structured gate pass/fail result
- ExitType/ExitDecision: Exit classification (safety vs invalidation)
- SymbolCharacteristics: Rolling statistics for symbol-adaptive parameters
"""

