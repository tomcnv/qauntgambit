class TestProperty17PipelineStages:
    """
    Feature: copilot-deep-knowledge, Property 17: System prompt includes all pipeline stages

    For any system prompt generated by SystemPromptBuilder, the prompt text
    SHALL contain references to all pipeline stages from CANONICAL_STAGE_ORDER
    plus all stage files in the stages directory.

    **Validates: Requirements 1.1**
    """

    @given(data=tool_registry_strategy())
    @settings(max_examples=100)
    def test_all_pipeline_stages_present(self, data):
        """Regardless of which tools are registered, all pipeline stages appear."""
        registry, _tools = data
        builder = SystemPromptBuilder(registry)
        prompt = builder.build()

        for stage in PIPELINE_STAGES:
            assert stage in prompt, (
                f"Pipeline stage '{stage}' missing from system prompt"
            )

    @given(data=tool_registry_strategy(), ctx=trade_context_strategy())
    @settings(max_examples=100)
    def test_all_pipeline_stages_present_with_trade_context(self, data, ctx):
        """Pipeline stages remain present even when trade context is injected."""
        registry, _tools = data
        builder = SystemPromptBuilder(registry)
        prompt = builder.build(trade_context=ctx)

        for stage in PIPELINE_STAGES:
            assert stage in prompt, (
                f"Pipeline stage '{stage}' missing from system prompt with trade context"
            )


