# The canonical order as defined by get_canonical_stage_order()
CANONICAL_STAGE_ORDER = [
    "data_readiness",
    "amt_calculator",
    "global_gate",
    "profile_routing",
    "signal_check",
    "arbitration",
    "confirmation",
    "ev_gate",
    "execution_feasibility",
    "execution",
]


def _find_stage_heading(prompt: str, stage_name: str) -> int:
    """Find the position of a stage's heading entry in the prompt.

    Stages are formatted as ``**stage_name (`` in the prompt (e.g.
    ``**data_readiness (DataReadinessStage)**``).  We search for the
    heading marker to avoid false positives from incidental mentions of
    the stage name in other descriptions.
    """
    marker = f"**{stage_name} ("
    pos = prompt.find(marker)
    if pos != -1:
        return pos
    # Fallback: try bold marker without parenthetical class name
    marker_alt = f"**{stage_name}"
    return prompt.find(marker_alt)


class TestProperty2SystemPromptPreservesCanonicalOrdering:
    """
    Feature: copilot-deep-knowledge, Property 2: System prompt preserves canonical stage ordering

    For any pair of stages (A, B) where A precedes B in the canonical stage
    order, the first occurrence of A's name in the system prompt SHALL appear
    before the first occurrence of B's name. We locate each stage by its
    heading entry (``**stage_name (``) to avoid false positives from
    incidental mentions in other stage descriptions.

    **Validates: Requirements 1.2**
    """

    @given(data=tool_registry_strategy())
    @settings(max_examples=100)
    def test_canonical_ordering_preserved(self, data):
        """For any random tool registry, canonical stage order is preserved in the prompt."""
        registry, _tools = data
        builder = SystemPromptBuilder(registry)
        prompt = builder.build()

        for i in range(len(CANONICAL_STAGE_ORDER)):
            for j in range(i + 1, len(CANONICAL_STAGE_ORDER)):
                stage_a = CANONICAL_STAGE_ORDER[i]
                stage_b = CANONICAL_STAGE_ORDER[j]
                pos_a = _find_stage_heading(prompt, stage_a)
                pos_b = _find_stage_heading(prompt, stage_b)
                assert pos_a != -1, (
                    f"Stage heading for '{stage_a}' not found in system prompt"
                )
                assert pos_b != -1, (
                    f"Stage heading for '{stage_b}' not found in system prompt"
                )
                assert pos_a < pos_b, (
                    f"Canonical ordering violated: '{stage_a}' (pos {pos_a}) "
                    f"should appear before '{stage_b}' (pos {pos_b})"
                )

    @given(
        data=tool_registry_strategy(),
        pair_idx=st.tuples(
            st.integers(min_value=0, max_value=len(CANONICAL_STAGE_ORDER) - 2),
            st.integers(min_value=1, max_value=len(CANONICAL_STAGE_ORDER) - 1),
        ).filter(lambda t: t[0] < t[1]),
    )
    @settings(max_examples=100)
    def test_random_pair_ordering_preserved(self, data, pair_idx):
        """For any random pair (A, B) where A precedes B, ordering holds."""
        registry, _tools = data
        idx_a, idx_b = pair_idx
        stage_a = CANONICAL_STAGE_ORDER[idx_a]
        stage_b = CANONICAL_STAGE_ORDER[idx_b]

        builder = SystemPromptBuilder(registry)
        prompt = builder.build()

        pos_a = _find_stage_heading(prompt, stage_a)
        pos_b = _find_stage_heading(prompt, stage_b)
        assert pos_a != -1, f"Stage heading for '{stage_a}' not found in system prompt"
        assert pos_b != -1, f"Stage heading for '{stage_b}' not found in system prompt"
        assert pos_a < pos_b, (
            f"Canonical ordering violated: '{stage_a}' (pos {pos_a}) "
            f"should appear before '{stage_b}' (pos {pos_b})"
        )
