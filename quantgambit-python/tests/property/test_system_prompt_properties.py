# Non-empty printable strings for tool names and descriptions.
# We use a restricted alphabet to avoid control characters that would never
# appear in real tool names/descriptions, while still exercising variety.
_tool_name_text = st.text(
    alphabet=st.characters(whitelist_categories=("L", "N"), whitelist_characters="_-"),
    min_size=1,
    max_size=40,
).filter(lambda s: s.strip())

_tool_desc_text = st.text(
    alphabet=st.characters(whitelist_categories=("L", "N", "Z", "P")),
    min_size=1,
    max_size=100,
).filter(lambda s: s.strip())


async def _noop_handler(**kwargs):
    return {}


@st.composite
def tool_definition_strategy(draw):
    """Generate a ToolDefinition with random name and description."""
    name = draw(_tool_name_text)
    description = draw(_tool_desc_text)
    return ToolDefinition(
        name=name,
        description=description,
        parameters_schema={"type": "object", "properties": {}},
        handler=_noop_handler,
    )


@st.composite
def tool_registry_strategy(draw):
    """Generate a ToolRegistry populated with 0-10 random tools."""
    tools = draw(
        st.lists(tool_definition_strategy(), min_size=0, max_size=10, unique_by=lambda t: t.name)
    )
    registry = ToolRegistry()
    for tool in tools:
        registry.register(tool)
    return registry, tools


# Strategy for non-empty identifier-like strings (trade IDs, symbols, etc.)
_identifier_text = st.text(
    alphabet=st.characters(whitelist_categories=("L", "N"), whitelist_characters="-_"),
    min_size=1,
    max_size=30,
).filter(lambda s: s.strip())

_side_strategy = st.sampled_from(["BUY", "SELL", "buy", "sell", "Buy", "Sell"])

_price_strategy = st.floats(
    min_value=0.0001, max_value=1_000_000.0, allow_nan=False, allow_infinity=False
)

_pnl_strategy = st.floats(
    min_value=-1_000_000.0, max_value=1_000_000.0, allow_nan=False, allow_infinity=False
)

_hold_time_strategy = st.floats(
    min_value=0.0, max_value=1_000_000.0, allow_nan=False, allow_infinity=False
)


@st.composite
def trade_context_strategy(draw):
    """Generate an arbitrary TradeContext with random field values."""
    return TradeContext(
        trade_id=draw(_identifier_text),
        symbol=draw(_identifier_text),
        side=draw(_side_strategy),
        entry_price=draw(_price_strategy),
        exit_price=draw(_price_strategy),
        pnl=draw(_pnl_strategy),
        hold_time_seconds=draw(_hold_time_strategy),
        decision_trace_id=draw(st.one_of(st.none(), _identifier_text)),
    )


